nchf_tc( N, IP, Port, JSON ) when N > 0 ->
	{ok, Pid} = gun:open( IP, Port, #{protocols => [http2]} ),
    {ok, http2} = gun:await_up(Pid),
	Path = <<"/nchf-offlineonlycharging/v1/offlinechargingdata">>,
	Header = #{<<"content-type">> => <<"application/json">>},
	Result = timer:tc( fun () -> nchf_loop( N, {Pid, Path, Header, JSON} ) end ),
	gun:shutdown( Pid ),
	Result.

  %
  % Internal functions
  %

nchf_loop( 0, _ ) ->
	ok;
nchf_loop( N, {Pid, Path, Header, JSON} = Values ) ->
	Request = jsx:encode(JSON#{<<"invocationSequenceNumber">> => N, <<"invocationTimestamp">> => now()}),
	Stream = gun:post( Pid, Path, Header, Request ),
	{response, nofin, 201, _Headers} = gun:await( Pid, Stream ),
	{ok, Body} = gun:await_body( Pid, Stream ),
	Response = jsx:decode( Body ),
	#{<<"invocationSequenceNumber">> := N} = Response,
	nchf_loop( N - 1, Values ).

now() -> os:system_time( second ).

